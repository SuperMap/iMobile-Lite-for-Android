package com.supermap.imobilelite.maps.query;

/**
 * <p>
 * 字段类型枚举类。 
 * </p>
 * <p>
 * 该类定义了一系列的字段类型。
 * </p>
 * @author ${Author}
 * @version ${Version}
 *
 */
public enum FieldType {

    /**
     * <p>
     * 布尔类型。
     * </p>
     */
    BOOLEAN,

    /**
     * <p>
     * 字节类型。
     * </p>
     */
    BYTE,

    /**
     * <p>
     * 16 位整数类型。
     * </p>
     */
    INT16,

    /**
     * <p>
     * 32 位整数类型。
     * </p>
     */
    INT32,

    /**
     * <p>
     * 64 位整数类型。
     * </p>
     */
    INT64,
    /**
     * <p>
     * 单精度浮点类型。
     * </p>
     */
    SINGLE,
    /**
     * <p>
     * 双精度浮点类型。
     * </p>
     */
    DOUBLE,

    /**
     * <p>
     * 日期类型。
     * </p>
     */
    DATETIME,

    /**
     * <p>
     * 任意长度的二进制数据类型。
     * </p>
     */
    LONGBINARY,

    /**
     * <p>
     * 文本类型。
     * </p>
     */
    TEXT,
    /**
     * <p>
     * 宽字符类型字段。
     * </p>
     */
    WTEXT,

    /**
     * <p>
     * 字符类型。
     * </p>
     */
    CHAR

}
