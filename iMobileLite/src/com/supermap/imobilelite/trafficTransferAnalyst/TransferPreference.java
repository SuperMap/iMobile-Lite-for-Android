package com.supermap.imobilelite.trafficTransferAnalyst;

/**
 * <p>
 * 交通换乘时乘车偏好枚举类。
 * </p>
 * @author ${Author}
 * @version ${Version}
 *
 */
public enum TransferPreference {
    /**
     * <p>
     * 公交汽车优先。
     * </p>
     */
    BUS,
    /**
     * <p>
     * 无乘车偏好。
     * </p>
     */
    NONE,
    /**
     * <p>
     * 不乘坐地铁。
     * </p>
     */
    NO_SUBWAY,
    /**
     * <p>
     * 地铁优先。
     * </p>
     */
    SUBWAY;

}
