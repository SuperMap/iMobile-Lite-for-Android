package com.supermap.imobilelite.theme;

/**
 * <p>
 * 标签专题图中标签的背景形状枚举类。
 * </p>
 * @author ${Author}
 * @version ${Version}
 * 
 */
public enum LabelBackShape {

    /**
     * <p>
     * 菱形背景，即标签背景的形状为菱形。
     * </p>
     */
    DIAMOND,

    /**
     * <p>
     * 椭圆形背景，即标签背景的行状为椭圆形。
     * </p>
     */
    ELLIPSE,

    /**
     * <p>
     * 符号背景，即标签背景的形状为设定的符号。
     * </p>
     */
    MARKER,

    /**
     * <p>
     * 空背景，即不使用任何形状作为标签的背景。此为默认值。
     * </p>
     */
    NONE,

    /**
     * <p>
     * 矩形背景，即标签背景的形状为矩形。
     * </p>
     */
    RECT,

    /**
     * <p>
     * 圆角矩形背景，即标签背景的形状为圆角矩形。
     * </p>
     */
    ROUNDRECT,

    /**
     * <p>
     * 三角形背景，即标签背景的形状为三角形。
     * </p>
     */
    TRIANGLE;

}
