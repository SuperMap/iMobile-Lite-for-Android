package com.supermap.imobilelite.networkAnalyst;

/**
 * <p>
 * 图片格式枚举类。
 * </p>
 * @author ${Author}
 * @version ${Version}
 *
 */
public enum OutputFormat {
    /**
     * <p>
     * BINARY 格式。
     * </p>
     */
    BINARY,

    /**
     * <p>
     * BMP 格式。
     * </p>
     */
    BMP,

    /**
     * <p>
     *  默认的图片输出格式，即 PNG 格式。
     * </p>
     */
    DEFAULT,

    /**
     * <p>
     * EMF 格式，该格式暂不支持。
     * </p>
     */
    EMF,

    /**
     * <p>
     * EPS 格式，该格式暂不支持。
     * </p>
     */
    EPS,

    /**
     * <p>
     *  GIF 格式。
     * </p>
     */
    GIF,

    /**
     * <p>
     * JPG 格式。
     * </p>
     */
    JPG,

    /**
     * <p>
     * JPG_PNG 混合格式。
     * </p>
     */
    JPG_PNG,

    /**
     * <p>
     *  PDF 格式，该格式暂不支持。
     * </p>
     */
    PDF,

    /**
     * <p>
     * PNG 格式。
     * </p>
     */
    PNG;

}
